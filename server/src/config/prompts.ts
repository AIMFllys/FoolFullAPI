export function buildDeepReasoningPrompt(userMessage: string, sources: SearchSourceItem[]): string {
  const sourceContext = sources
    .map((item, index) => {
      const sourceName = item.source || safeHostname(item.url);
      return [
        `来源 ${index + 1}: ${sourceName}`,
        `标题: ${item.title}`,
        `链接: ${item.url}`,
        `摘要: ${item.content || '无摘要'}`,
      ].join('\n');
    })
    .join('\n\n');

  return `你是一名严谨的中文研究助理。你将基于用户问题和联网搜索得到的资料，输出四段可展示的推理内容，供前端分别显示为 think、plan、review 和 answer。
你必须只输出以下私有标记，不得输出 XML、Markdown 标题或额外说明：
<<THINK>>
...
<</THINK>>
<<PLAN>>
...
<</PLAN>>
<<REVIEW>>
...
<</REVIEW>>
<<ANSWER>>
...
<</ANSWER>>

硬性要求：
1. 四段顺序必须固定，且全部出现。
2. think、plan、review 中禁止出现节日彩蛋、整蛊、演示脚本、系统伪装等内容。
3. answer 必须真正回答用户问题，可使用 Markdown，但不要输出 HTML 标签。
4. think、plan、review 各控制在 40 到 120 字，语气自然可信。
5. answer 必须以中文输出，并优先基于给定资料。
6. 不要输出尖括号 XML 标签，不要输出 JSON，不要输出多余前后缀。

用户问题：
${userMessage}

可参考资料：
${sourceContext}`;
}

function safeHostname(url: string): string {
  try {
    return new URL(url).hostname.replace(/^www\./, '');
  } catch {
    return '未知来源';
  }
}
